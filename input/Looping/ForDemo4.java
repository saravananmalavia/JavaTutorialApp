public class ForDemo4{
	public static void main(String[] args){
	
	int x = 10;		

		// for - entry check
		for(x=10;x>10;x++){
			System.out.println("I am with in for x: " +x);
		}
		// while - entry check
		while(x>10){
			System.out.println("I am with in while x: " +x);
		//	x++;
		}
		// do - entry check
		do{
			System.out.println("I am with in  do while x: " +x);
			//x++;
		}while(x >10);



	}		
	
}