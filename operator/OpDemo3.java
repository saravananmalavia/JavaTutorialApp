public class OpDemo3{
	public static void main(String[] args){

		int x = 325;
		int y = x << 1; // x * 2
		int z = x >> 2; // x /2
		 //>>>


		//00000001

		System.out.println("x  : " + x);
		System.out.println("x << 1 : " + y);
		System.out.println("x >> 1 : " + z);	
	

	}

}

